//  4. Logical Operators

let age = 20;
let hasID = true;

console.log(age > 18 && hasID); // AND
console.log(age > 18 || false); // OR
console.log(!hasID);            // NOT


console.log(true && "hello");  // hello
console.log(false && "hello"); // false
console.log(true || "hi");     // true
console.log(false || "hi");    // hi
