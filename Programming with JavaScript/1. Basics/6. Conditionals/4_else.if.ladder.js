/*
========================================
Else If Ladder Examples
========================================
*/

// Example 1
let marks = 82;

if(marks >= 90){
    console.log("Grade A");
}
else if(marks >= 75){
    console.log("Grade B");
}
else if(marks >= 60){
    console.log("Grade C");
}
else{
    console.log("Fail");
}



// Example 2
