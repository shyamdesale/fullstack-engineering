/*
========================================
Ternary Operator Examples
========================================
*/

// Example 1
let age = 18;

let result = age >= 18 ? "Adult" : "Minor";

console.log(result);



// Example 2
let number = 7;

let type = number % 2 === 0 ? "Even" : "Odd";

console.log(type);



// Example 3
let balance = 500;

let message = balance >= 1000 ? "Transaction Allowed" : "Insufficient Balance";

console.log(message);



// Example 4
let marks = 75;

let grade = marks >= 60 ? "Pass" : "Fail";

console.log(grade);



// Example 5
let isLoggedIn = true;

let status = isLoggedIn ? "Welcome User" : "Please Login";

console.log(status);