// Definition: 
// at() array ke specified index ka element return karta hai. Iski khas baat ye hai ki negative index bhi support karta hai.

let students = [
    "Shyam",
    "Rahul",
    "Amit",
    "Priya"
];

console.log("Students:", students);

console.log("First Student:", students.at(0));
console.log("Second Student:", students.at(1));

// Students:
// [ 'Shyam', 'Rahul', 'Amit', 'Priya' ]

// First Student: Shyam
// Second Student: Rahul