// Definition: 
// reverse() array ke elements ka order ulta (reverse) kar deta hai. Ye original array ko modify karta hai.

let students = [
    "Shyam",
    "Rahul",
    "Amit",
    "Priya"
];

console.log("Before:", students);

students.reverse();

console.log("After:", students);

// Before:
// [ 'Shyam', 'Rahul', 'Amit', 'Priya' ]

// After:
// [ 'Priya', 'Amit', 'Rahul', 'Shyam' ]