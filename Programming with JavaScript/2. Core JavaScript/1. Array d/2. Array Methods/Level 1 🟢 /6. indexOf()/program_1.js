// Definition: 
// indexOf() array me kisi element ka first index (position) return karta hai. Agar element nahi milta, to -1 return karta hai.

let students = ["Shyam", "Rahul", "Amit", "Priya"];

let studentIndex = students.indexOf("Amit");

console.log("Students:", students);
console.log("Index of Amit:", studentIndex);

// Students: [ 'Shyam', 'Rahul', 'Amit', 'Priya' ]
// Index of Amit: 2